//single line comment
/* Multi line comment */

#include <stdio.h> //Header files

int main()
{
    printf("Hello World \t"); //Escape character = \t Gives you a tab space , Next line = \n
    printf("Good Morning \n");
    printf("This is a double \"\" quote \n"); // \" Will display " on the screen
    printf("This is a backslash \\ "); // \\ will display \ on the screen
    printf("\nThis is a single quote ' \n");
    printf("Escape characters are \\n \\t \\\" \\\\"); // Printing escape characters on the screen

    return 0;

}